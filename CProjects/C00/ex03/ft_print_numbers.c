#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char	nums;

	nums = '0';
	while (nums <= '9')
	{
		ft_putchar(nums);
		nums++;
	}
}
